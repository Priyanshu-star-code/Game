# sort a list of integers in descending order?
integer_list = [5, 2, 8, 1, 9, 3 , 3 ]

# Sort the list in decreasing order
# reverse=True parameter is used to sort the list in descending order.
sorted_list = sorted(integer_list, reverse=True)

# Print the sorted list
print("Sorted list in decreasing order:", sorted_list)